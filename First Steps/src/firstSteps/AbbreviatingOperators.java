package firstSteps;

public class AbbreviatingOperators {

	public static void main(String[] args) {
		
		int result = 1;
		
		result = result + 1;
		
		System.out.println("Result = "+result);
		
		result++;
		
		System.out.println(result);
		
		result--;
		
		System.out.println(result);
		
		result -= 1;
		
		System.out.println(result);
		
		result += 1;
		
		System.out.println(result);
		
		result += 5;
		
		System.out.println(result);
		
		int result2 = 10;

		result2 -= 7;
		
		System.out.println(result2);
		
		result2 = 10;
		
		result2 -= 5.5;
		
		System.out.println(result2);
		
		result2 = 10;
		
		result2 = (int)(result2 - 5.5);
		
		System.out.println(result2);
		
		double result3 = 10.0;
		
		result3 -= 5.5;
		
		System.out.println(result3);
		
		result3 = 10.0;
		
		result3 *= 1.5;
		
		System.out.println(result3);
		
		result3 = 10.0;
		
		result3 /= 1.5;
		
		System.out.println(result3);
	}

}
