package firstSteps;

public class CharAndBooleanPrimitiveDataTypes {

	public static void main(String[] args) {
		
		char myChar = 'D';
		
		System.out.println(myChar);
		
		char myUnicode = '\u0044';
		
		System.out.println(myUnicode);
		
		char myDecimalCode = 68;
		
		System.out.println(myDecimalCode);

		char mySimpleChar = '?';
		
		System.out.println(mySimpleChar);
		
		char myUnicodeChar = '\u003F';
		
		System.out.println(myUnicodeChar);
		
		char myDecimalChar = 63;
		
		System.out.println(myDecimalChar);
		
		System.out.println("My values are "+mySimpleChar+myUnicodeChar+myDecimalChar);
		
		boolean myTrueBoolean = true;
		
		System.out.println(myTrueBoolean);
		
		boolean myFalseBoolean = false;
		
		System.out.println(myFalseBoolean);
		
		boolean isMyCostumerOverTwentyOne = true;
		
		System.out.println(isMyCostumerOverTwentyOne);
		
		
		
	}

}
